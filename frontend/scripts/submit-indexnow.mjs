#!/usr/bin/env node
/**
 * IndexNow submission — proactively notify Bing, Yandex, and other IndexNow
 * participants (Google does NOT support IndexNow, but Bing/Yandex traffic is
 * non-trivial in CN + Russia). Site verification needs a stable key file.
 *
 * Usage:
 *   node scripts/submit-indexnow.mjs                    # submit all indexed URLs from sitemaps
 *   node scripts/submit-indexnow.mjs --recent 7         # only URLs updated in last 7 days
 *   node scripts/submit-indexnow.mjs --urls a.com,b.com # submit specific URLs (comma-separated)
 *   node scripts/submit-indexnow.mjs --urls-file path   # submit URLs from a file (one per line)
 *   node scripts/submit-indexnow.mjs --dry-run          # preview without submitting
 *
 * Key sourcing (in priority order):
 *   1. INDEXNOW_KEY env var
 *   2. First stable key file in public/ (committed, persists across builds)
 *   3. Generate new UUID and save to public/
 */

import { readFileSync, writeFileSync, existsSync, readdirSync, copyFileSync } from "fs";
import { join } from "path";
import { randomUUID } from "crypto";

const SITE = "https://agentskillshub.top";
const DIST = "dist";
const PUBLIC_DIR = "public";
const INDEXNOW_ENDPOINT = "https://api.indexnow.org/indexnow";

// Only sitemaps that are actually generated by generate-sitemap.mjs.
// See that file for the canonical list — keep in sync when adding/removing.
const ACTIVE_SITEMAPS = [
  "sitemap-top.xml",
  "sitemap-categories.xml",
  "sitemap-static.xml",
  "sitemap-scenarios.xml",
  "sitemap-comparisons.xml",
];

function parseArgs() {
  const args = process.argv.slice(2);
  const getValue = (flag) => {
    const i = args.indexOf(flag);
    return i >= 0 ? args[i + 1] : undefined;
  };
  return {
    dryRun: args.includes("--dry-run"),
    recentDays: args.includes("--recent")
      ? parseInt(getValue("--recent") || "7", 10)
      : 0,
    urls: getValue("--urls"),
    urlsFile: getValue("--urls-file"),
  };
}

/** Find a stable IndexNow key — prefer existing committed key file in public/. */
function getOrCreateKey() {
  // 1. Env var (highest priority)
  const envKey = process.env.INDEXNOW_KEY;
  if (envKey && /^[a-f0-9]{32}$/i.test(envKey)) {
    ensureKeyFilesInDist(envKey);
    return envKey;
  }

  // 2. Existing committed key file in public/
  try {
    const publicFiles = readdirSync(PUBLIC_DIR);
    const keyFile = publicFiles.find((f) => /^[a-f0-9]{32}\.txt$/i.test(f));
    if (keyFile) {
      const key = keyFile.replace(".txt", "");
      const body = readFileSync(join(PUBLIC_DIR, keyFile), "utf-8").trim();
      if (body === key) {
        ensureKeyFilesInDist(key);
        return key;
      }
    }
  } catch {
    // public/ may not exist in some contexts
  }

  // 3. Last resort — generate new and save to public/ for future builds
  const key = randomUUID().replace(/-/g, "");
  console.log(`Generated new IndexNow key: ${key}. Committing to public/ so it persists.`);
  writeFileSync(join(PUBLIC_DIR, `${key}.txt`), key);
  ensureKeyFilesInDist(key);
  return key;
}

/** Make sure {key}.txt and indexnow-key.txt are in dist/ (Vite may have already done it). */
function ensureKeyFilesInDist(key) {
  const keyFileInDist = join(DIST, `${key}.txt`);
  if (!existsSync(keyFileInDist)) {
    writeFileSync(keyFileInDist, key);
  }
  writeFileSync(join(DIST, "indexnow-key.txt"), key);
}

function extractUrlsFromSitemap(sitemapPath) {
  if (!existsSync(sitemapPath)) return [];
  const content = readFileSync(sitemapPath, "utf-8");
  const urls = [];
  const locRegex = /<loc>(.*?)<\/loc>/g;
  const lastmodRegex = /<lastmod>(.*?)<\/lastmod>/g;
  let locMatch, lastmodMatch;
  while ((locMatch = locRegex.exec(content)) !== null) {
    lastmodMatch = lastmodRegex.exec(content);
    urls.push({
      url: locMatch[1],
      lastmod: lastmodMatch ? lastmodMatch[1] : null,
    });
  }
  return urls;
}

async function submitBatch(urls, key) {
  const BATCH_SIZE = 10000;
  let submitted = 0;

  for (let i = 0; i < urls.length; i += BATCH_SIZE) {
    const batch = urls.slice(i, i + BATCH_SIZE);
    const payload = {
      host: "agentskillshub.top",
      key,
      keyLocation: `${SITE}/${key}.txt`,
      urlList: batch,
    };

    const res = await fetch(INDEXNOW_ENDPOINT, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(payload),
    });

    submitted += batch.length;
    console.log(
      `Batch ${Math.floor(i / BATCH_SIZE) + 1}: ${res.status} ${res.statusText} (${batch.length} URLs)`,
    );

    if (res.status !== 200 && res.status !== 202) {
      const text = await res.text().catch(() => "");
      console.error(`  Error: ${text}`);
    }
  }

  return submitted;
}

async function main() {
  const { dryRun, recentDays, urls: urlsArg, urlsFile } = parseArgs();

  let urlStrings = [];

  // Priority 1: explicit --urls or --urls-file (e.g., from daily report)
  if (urlsArg) {
    urlStrings = urlsArg
      .split(",")
      .map((u) => u.trim())
      .filter(Boolean);
    console.log(`Submitting ${urlStrings.length} URLs from --urls argument`);
  } else if (urlsFile && existsSync(urlsFile)) {
    urlStrings = readFileSync(urlsFile, "utf-8")
      .split(/\r?\n/)
      .map((u) => u.trim())
      .filter((u) => u && u.startsWith("http"));
    console.log(`Submitting ${urlStrings.length} URLs from ${urlsFile}`);
  } else {
    // Default: collect from sitemaps
    let allUrls = [];
    for (const file of ACTIVE_SITEMAPS) {
      const entries = extractUrlsFromSitemap(join(DIST, file));
      allUrls.push(...entries);
    }
    console.log(`Found ${allUrls.length} total URLs in sitemaps`);

    if (recentDays > 0) {
      const cutoff = new Date();
      cutoff.setDate(cutoff.getDate() - recentDays);
      const cutoffStr = cutoff.toISOString().split("T")[0];
      allUrls = allUrls.filter((u) => u.lastmod && u.lastmod >= cutoffStr);
      console.log(`Filtered to ${allUrls.length} URLs updated in last ${recentDays} days`);
    }

    urlStrings = allUrls.map((u) => u.url);
  }

  if (urlStrings.length === 0) {
    console.log("No URLs to submit.");
    return;
  }

  if (dryRun) {
    console.log(`\n[DRY RUN] Would submit ${urlStrings.length} URLs:`);
    urlStrings.slice(0, 10).forEach((u) => console.log(`  ${u}`));
    if (urlStrings.length > 10)
      console.log(`  ... and ${urlStrings.length - 10} more`);
    return;
  }

  const key = getOrCreateKey();
  console.log(`Using IndexNow key: ${key.slice(0, 8)}... (verified at ${SITE}/${key}.txt)`);

  const submitted = await submitBatch(urlStrings, key);
  console.log(`\nDone: ${submitted} URLs submitted to IndexNow`);
}

main().catch(console.error);
